/**
 * strudelCompiler.js
 *
 * Converts the structured music JSON (from Claude) into idiomatic Strudel
 * live-coding JavaScript.
 *
 * Key design decisions:
 *   - Every note's duration is expressed as an @-weight so the total weights
 *     within each measure bracket sum to the correct beat count.
 *   - Eighth notes are grouped in pairs → [a b]  (1 beat each pair)
 *   - Sixteenth notes grouped in fours → [a b c d]  (1 beat each quad)
 *   - 32nd notes grouped in eights → [a b c d e f g h]  (1 beat each octet)
 *   - Triplets: eighth triplets → [a b c] (1 beat), quarter triplets → [a b c]@2 (2 beats)
 *   - Chords: consecutive notes with chord:true → [c4,e4,g4]
 *   - Multiple sections → arrange(), single section → direct $: patterns
 *   - Never uses .slow() on note patterns; always uses /N inside angle brackets
 */

